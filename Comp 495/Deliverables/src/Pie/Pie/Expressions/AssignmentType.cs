
namespace Pie.Expressions
{
    // The types of assignment: =, +=, -=, *=, /=, |=, &=
    public enum AssignmentType
    {
        Equal,          // =
        Add,            // +=
        Subtract,       // -=
        Divide,         // /=
        Multiply,       // *=
        Or,             // |=
        And             // &=
    }
}
